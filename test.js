import test from 'ava';
import m from './';

test(t => {
	t.is(m('10100011010110001001100100011010001101000110101010111001011001101101100100111011001101000100101'), true);
	t.is(m('101').code, 'LENGTH');
	t.is(m('10110101').code, 'LENGTH');
	// Mess up left guard
	t.is(m('11110011010110001001100100011010001101000110111010111001011001101101100100111011001101000100101').code, 'LEFT_GUARD');
	// Mess up right guard
	t.is(m('10110011010110001001100100011010001101000110111010111001011001101101100100111011001101000100111').code, 'RIGHT_GUARD');
	// Mess up center guard
	t.is(m('10110011010110001001100100011010001101000110111010111001011001101101100100111011001101000100101').code, 'CENTER_GUARD');
	// Mess up a number on the left
	t.is(m('10100011010100001001100100011010001101000110101010111001011001101101100100111011001101000100101').code, 'INCORRECT_NUMBER');
	// Mess up a number on the right
	t.is(m('10100011010110001001100100011010001101000110101010111001011001101101100100111011001101010100101').code, 'INCORRECT_NUMBER');
});
